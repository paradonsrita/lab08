      Founded in July 2014 by Brock Pierce, Craig Sellars, and Reeve Collins, Tether (USDT), originally known as "Realcoin", is a cryptocurrency commonly known as a “stablecoin” that aims to keep a fixed 1:1 exchange ratio with the U.S. dollar. Tether was one of the earliest stablecoins created, built upon Mastercoin (Omni), which was a protocol layer for Bitcoin that introduced the stablecoin concept to the world in 2012. Tether pioneered what is now known as a fiat-collateralized stablecoin model, and is now the most widely used stablecoin today.<br>
  
  <br>Tether was created as an attempt to solve two major issues with existing cryptocurrencies: high volatility and convertibility between fiat currencies and cryptocurrencies. To address these perceived issues Tether created a cryptocurrency that is fully backed 1:1 by deposits of U.S. dollars held at banks. On Ethereum, USDT tokens are represented as ERC-20 tokens, while on Bitcoin, Tether utilizes the Omni layer, to represent USDT tokens. While the tokens themselves operate in a decentralized network, Hong Kong based Tether Ltd is solely responsible for creating and redeeming tokens as well as maintaining the 1:1 deposit backing.<br>
  
  <br>Exchanges have been the primary users of USDT as an alternative to fiat currencies, reducing or eliminating the need to maintain outside banking relationships. Notable exchanges that utilize the token include Bitfinex, Bittrex, Binance, Coinbase, Kraken, Huobi, Poloniex, Liquid.io, Gate.io, and OKEx. Tether Ltd shares many executives and has strong ties with Hong Kong based Bitfinex with both sharing the same parent organization iFinex Inc.<br>
  
